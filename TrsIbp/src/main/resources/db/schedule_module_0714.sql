CREATE TABLE IF NOT EXISTS schdl_info (
    SCHDL_SN BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT '일정일련번호',
    CO_ID VARCHAR(20) NOT NULL COMMENT '회사ID',
    USER_ID VARCHAR(50) NOT NULL COMMENT '사용자아이디',
    BIZ_ID VARCHAR(20) NULL COMMENT '사업ID',
    SCHDL_SE_CD VARCHAR(30) NOT NULL COMMENT '일정구분코드',
    SCHDL_NM VARCHAR(200) NOT NULL COMMENT '일정명',
    SCHDL_CN VARCHAR(1000) NULL COMMENT '일정내용',
    SCHDL_BGNG_DT DATETIME NOT NULL COMMENT '일정시작일시',
    SCHDL_END_DT DATETIME NOT NULL COMMENT '일정종료일시',
    ALL_DAY_YN CHAR(1) NOT NULL DEFAULT 'N' COMMENT '종일여부',
    LOC_NM VARCHAR(200) NULL COMMENT '장소명',
    USE_YN CHAR(1) NOT NULL DEFAULT 'Y' COMMENT '사용여부',
    REG_DT DATETIME NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    MDFCN_DT DATETIME NULL COMMENT '수정일시',
    RGTR_ID VARCHAR(50) NULL COMMENT '등록자아이디',
    MDFR_ID VARCHAR(50) NULL COMMENT '수정자아이디',
    PRIMARY KEY (SCHDL_SN),
    KEY IDX_SCHDL_CO_DT (CO_ID, SCHDL_BGNG_DT, SCHDL_END_DT),
    KEY IDX_SCHDL_USER_DT (USER_ID, SCHDL_BGNG_DT),
    KEY IDX_SCHDL_BIZ (BIZ_ID),
    CONSTRAINT FK_SCHDL_CO FOREIGN KEY (CO_ID) REFERENCES co_info (CO_ID),
    CONSTRAINT FK_SCHDL_USER FOREIGN KEY (USER_ID) REFERENCES user_info (USER_ID),
    CONSTRAINT FK_SCHDL_BIZ FOREIGN KEY (BIZ_ID) REFERENCES biz_info (BIZ_ID) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='일정정보';

