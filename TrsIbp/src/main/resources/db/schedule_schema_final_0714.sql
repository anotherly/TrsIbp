/* =========================================================
 * 종합 일정 캘린더 스키마 최종 보정 SQL
 * - R40/R41 적용 과정에서 schdl_info에 기존 컬럼과 신규 컬럼이 혼재한 DB를 보정한다.
 * - 기존 화면/쿼리 호환을 위해 신규 컬럼(BGNG_DT/END_DT/PLACE_NM)과
 *   기존 컬럼(SCHDL_BGNG_DT/SCHDL_END_DT/LOC_NM)을 모두 유지하고 값을 동기화한다.
 * ========================================================= */

ALTER TABLE schdl_info
    ADD COLUMN IF NOT EXISTS BGNG_DT DATETIME NULL COMMENT '시작일시' AFTER SCHDL_NM,
    ADD COLUMN IF NOT EXISTS END_DT DATETIME NULL COMMENT '종료일시' AFTER BGNG_DT,
    ADD COLUMN IF NOT EXISTS SCHDL_BGNG_DT DATETIME NULL COMMENT '일정시작일시' AFTER SCHDL_CN,
    ADD COLUMN IF NOT EXISTS SCHDL_END_DT DATETIME NULL COMMENT '일정종료일시' AFTER SCHDL_BGNG_DT,
    ADD COLUMN IF NOT EXISTS PLACE_NM VARCHAR(100) NULL COMMENT '장소명' AFTER ALL_DAY_YN,
    ADD COLUMN IF NOT EXISTS LOC_NM VARCHAR(200) NULL COMMENT '장소명' AFTER PLACE_NM,
    ADD COLUMN IF NOT EXISTS USER_ID VARCHAR(50) NULL COMMENT '기존 사용자아이디' AFTER CO_ID,
    ADD COLUMN IF NOT EXISTS BIZ_ID VARCHAR(20) NULL COMMENT '사업ID' AFTER USER_ID;

UPDATE schdl_info
SET BGNG_DT = IFNULL(BGNG_DT, SCHDL_BGNG_DT),
    END_DT = IFNULL(END_DT, SCHDL_END_DT),
    SCHDL_BGNG_DT = IFNULL(SCHDL_BGNG_DT, BGNG_DT),
    SCHDL_END_DT = IFNULL(SCHDL_END_DT, END_DT),
    PLACE_NM = IFNULL(PLACE_NM, LOC_NM),
    LOC_NM = IFNULL(LOC_NM, PLACE_NM);

ALTER TABLE schdl_info
    MODIFY COLUMN BGNG_DT DATETIME NOT NULL COMMENT '시작일시',
    MODIFY COLUMN END_DT DATETIME NOT NULL COMMENT '종료일시',
    MODIFY COLUMN SCHDL_BGNG_DT DATETIME NOT NULL COMMENT '일정시작일시',
    MODIFY COLUMN SCHDL_END_DT DATETIME NOT NULL COMMENT '일정종료일시',
    MODIFY COLUMN USER_ID VARCHAR(50) NULL COMMENT '기존 사용자아이디';

CREATE TABLE IF NOT EXISTS schdl_user_rel (
    SCHDL_SN BIGINT NOT NULL COMMENT '일정일련번호',
    USER_ID VARCHAR(50) NOT NULL COMMENT '사용자아이디',
    REG_DT DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    PRIMARY KEY (SCHDL_SN, USER_ID),
    KEY IDX_SCHDL_USER_REL_USER (USER_ID),
    CONSTRAINT FK_SCHDL_USER_REL_SCHDL FOREIGN KEY (SCHDL_SN) REFERENCES schdl_info (SCHDL_SN) ON DELETE CASCADE,
    CONSTRAINT FK_SCHDL_USER_REL_USER FOREIGN KEY (USER_ID) REFERENCES user_info (USER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='일정사용자관계';

INSERT IGNORE INTO schdl_user_rel (SCHDL_SN, USER_ID, REG_DT)
SELECT S.SCHDL_SN, S.USER_ID, IFNULL(S.REG_DT, NOW())
FROM schdl_info S
JOIN user_info U ON U.USER_ID = S.USER_ID
WHERE S.USER_ID IS NOT NULL
  AND S.USER_ID <> '';

CREATE INDEX IF NOT EXISTS IDX_SCHDL_INFO_CO_DT ON schdl_info (CO_ID, BGNG_DT, END_DT);
CREATE INDEX IF NOT EXISTS IDX_SCHDL_INFO_SE ON schdl_info (SCHDL_SE_CD);

