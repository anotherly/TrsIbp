/*
 * 사업관리 전주 회의 수정사항 반영 SQL
 * - VAT 포함여부 추가
 * - 사업 대금지급방법 단계 테이블 추가
 * - 투입인력 투입률 추가
 */

ALTER TABLE biz_info
    ADD COLUMN IF NOT EXISTS VAT_INCL_YN CHAR(1) NULL COMMENT '부가가치세포함여부' AFTER CTRT_AMT;

ALTER TABLE biz_mnpw
    ADD COLUMN IF NOT EXISTS INPUT_RT DECIMAL(5,2) NULL DEFAULT 100.00 COMMENT '투입률' AFTER UNTPRC;

CREATE TABLE IF NOT EXISTS biz_give_mthd (
    BIZ_GIVE_MTHD_SN BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT '사업지급방법일련번호',
    BIZ_ID VARCHAR(20) NOT NULL COMMENT '사업ID',
    GIVE_MTHD_CD VARCHAR(30) DEFAULT NULL COMMENT '지급방법코드',
    GIVE_MTHD_CN VARCHAR(1000) DEFAULT NULL COMMENT '지급방법상세내용',
    SORT_SEQ INT(11) DEFAULT 0 COMMENT '정렬순서',
    USE_YN CHAR(1) NOT NULL DEFAULT 'Y' COMMENT '사용여부',
    REG_DT DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    MDFCN_DT DATETIME DEFAULT NULL COMMENT '수정일시',
    RGTR_ID VARCHAR(50) DEFAULT NULL COMMENT '등록자아이디',
    MDFR_ID VARCHAR(50) DEFAULT NULL COMMENT '수정자아이디',
    PRIMARY KEY (BIZ_GIVE_MTHD_SN),
    KEY IDX_BIZ_GIVE_MTHD_BIZ (BIZ_ID, SORT_SEQ),
    CONSTRAINT FK_BIZ_GIVE_MTHD_BIZ FOREIGN KEY (BIZ_ID) REFERENCES biz_info (BIZ_ID) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='사업지급방법';

INSERT INTO cmn_cd (CD_GROUP_ID, CD, CD_NM, CD_EXPLN, SORT_SEQ, DFLT_YN, USE_YN, RMRK_CN, REG_DT)
SELECT 'GIVE_MTHD_CD', 'PROGRESS', '기성', '기성 지급', 40, 'N', 'Y', NULL, NOW()
WHERE NOT EXISTS (
    SELECT 1 FROM cmn_cd WHERE CD_GROUP_ID = 'GIVE_MTHD_CD' AND CD = 'PROGRESS'
);

INSERT INTO biz_give_mthd (BIZ_ID, GIVE_MTHD_CD, GIVE_MTHD_CN, SORT_SEQ, USE_YN, REG_DT)
SELECT B.BIZ_ID, B.GIVE_MTHD_CD, B.GIVE_MTHD_CN, 1, 'Y', NOW()
FROM biz_info B
WHERE (B.GIVE_MTHD_CD IS NOT NULL OR IFNULL(B.GIVE_MTHD_CN, '') <> '')
  AND NOT EXISTS (
      SELECT 1 FROM biz_give_mthd G WHERE G.BIZ_ID = B.BIZ_ID AND G.USE_YN = 'Y'
  );
